#===============================================================================
# IMPORTS
#===============================================================================

import sys

from PyQt4 import QtGui

from yatel import uis


#===============================================================================
# FUNCTION
#===============================================================================

def main():
    """Run Qt application"""
    app = QtGui.QApplication(sys.argv)
    w = uis.MainWindow()
    w.show()
    sys.exit(app.exec_())    


#===============================================================================
# MAIN
#===============================================================================

if __name__ == "__main__":
    main()
