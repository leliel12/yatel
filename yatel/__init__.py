import constants

#===============================================================================
# OLD META
#===============================================================================

for k, v in vars(constants).items():
    if k.isupper():
        metaname = "__{}__".format(k.lower())
        locals()[metaname] = v
del k
del v
del metaname


#===============================================================================
# MAIN
#===============================================================================

if __name__ == "__main__":
    print(__doc__)
